int month = 3;

switch (month) {
    case 1:
        System.out.println("Январь");
        break;
    case 2:
        System.out.println("Февраль");
        break;
    case 3:
        System.out.println("Март");  // выполняется эта ветка
        break;
    default:
        System.out.println("Неизвестный месяц");
}
