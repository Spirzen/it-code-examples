# Вывод значений от 0 до 9
for i in range(10):
    print(i, end=" ")
print()

# От 0 до 45 с шагом 5
for i in range(0, 50, 5):
    print(i, end=" ")
print()

# Обратный порядок — от 10 до 1
for i in range(10, 0, -1):
    print(i, end=" ")
print()

# Итерация по строке
for letter in 'word':
    print(letter)
